/** @type {Locale} */
let activeLocale = 'zh-CN';

function normalizeLocale(raw) {
    const s = String(raw || '').trim();
    if (s === 'en' || s.startsWith('en-')) return 'en';
    return 'zh-CN';
}

/** @param {Record<string, unknown>} bootstrap */
export function initI18n(bootstrap) {
    const saved = typeof localStorage !== 'undefined' ? localStorage.getItem(STORAGE_KEY) : null;
    const fromServer = bootstrap && typeof bootstrap.locale === 'string' ? bootstrap.locale : '';
    const nav =
        typeof navigator !== 'undefined' && navigator.language ? navigator.language : 'zh-CN';
    activeLocale = normalizeLocale(saved || fromServer || nav);
    if (typeof document !== 'undefined') {
        document.documentElement.lang = activeLocale === 'en' ? 'en' : 'zh-CN';
    }
}

export function getLocale() {
    return activeLocale;
}

/** @param {Locale} loc */
export function setLocale(loc) {
    const next = loc === 'en' ? 'en' : 'zh-CN';
    activeLocale = next;
    try {
        localStorage.setItem(STORAGE_KEY, next);
    } catch {
        /* ignore */
    }
    if (typeof document !== 'undefined') {
        document.documentElement.lang = next === 'en' ? 'en' : 'zh-CN';
    }
}

export function toggleLocale() {
    setLocale(activeLocale === 'en' ? 'zh-CN' : 'en');
    if (typeof location !== 'undefined') location.reload();
}

/** @param {string} key */
export function t(key) {
    const pack = DICT[activeLocale] || DICT['zh-CN'];
    const fallback = DICT['zh-CN'];
    return pack[key] ?? fallback[key] ?? key;
}
